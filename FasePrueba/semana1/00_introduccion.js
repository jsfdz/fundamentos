// JavaScript nació para “dar vida a las páginas web” y fue creado por Brendan Eich en 1995.

/*
¿Qué es JavaScript?
JavaScript (JS) es un lenguaje de programación ligero, interpretado, o compilado justo-a-tiempo (just-in-time) con funciones de primera clase. Si bien es más conocido como un lenguaje de scripting (secuencias de comandos) para páginas web, y es usado en muchos entornos fuera del navegador, tal como Node.js, Apache CouchDB y Adobe Acrobat. JavaScript es un lenguaje de programación basada en prototipos, multiparadigma, de un solo hilo, dinámico, con soporte para programación orientada a objetos, imperativa y declarativa (por ejemplo programación funcional).
*/

/*
- Los programas en este lenguaje se llaman scripts.
- Se pueden escribir directamente en el HTML de una página web y ejecutarse automáticamente a medida que se carga la página.
- Los scripts se proporcionan y ejecutan como texto plano.
- No necesitan preparación especial o compilación para correr.
*/

/*
¿Qué hace a JavaScript único?

Existen al menos tres cosas geniales sobre JavaScript:
  - Completa integración con HTML y CSS.
  - Las cosas simples se hacen de manera simple.
  - Soportado por la mayoría de los navegadores y habilitado de forma predeterminada.

JavaScript es la única tecnología de los navegadores que combina estas tres cosas.
Eso es lo que hace a JavaScript único. Por esto es la herramienta mas extendida para crear interfaces de navegador.
*/

/*
Especificación
La especificación ECMA-262 contiene la información más exhaustiva, detallada, y formal sobre JavaScript. En ella se define el lenguaje.

ECMA-262: https://www.ecma-international.org/publications/standards/Ecma-262.htm

Manuales
MDN (Mozilla) JavaScript Reference es el manual principal, con ejemplos y otras informaciones. Es fantástico para obtener información exhaustiva sobre funciones individuales del lenguaje, métodos, etc.

Se puede acceder en https://developer.mozilla.org/en-US/docs/Web/JavaScript/Reference.
*/

/*
Dato curioso:
¿Por qué se llama JavaScript?
Cuando JavaScript fue creado, inicialmente tenía otro nombre: “LiveScript”.
Pero Java era muy popular en ese momento, así que se decidió que el posicionamiento de un nuevo lenguaje como un “Hermano menor” de Java ayudaría.
Pero a medida que evolucionaba, JavaScript se convirtió en un lenguaje completamente independiente con su propia especificación llamada ECMAScript, y ahora no tiene ninguna relación con Java.
*/

/*
JavaScript fue inicialmente creado como un lenguaje solo para el navegador, pero ahora es usado también en muchos otros entornos.
Dicho esto, JavaScript también permite crear servidores, aplicaciones móviles, etc.
*/

